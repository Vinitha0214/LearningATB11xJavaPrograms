package Ex_04_Operators;

public class Lab038_Operators_Unary {
    public static void main(String[] args) {
        //Unary operator
        int a = +10;
        int a1 = -110;
        int result = a+a1;
        System.out.println(a);
        System.out.println(a1); // it print with minus value
        System.out.println(result);


        int b = -1;
        b = b+1;
        System.out.println(b);
    }
}
