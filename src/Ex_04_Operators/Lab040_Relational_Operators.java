package Ex_04_Operators;

public class Lab040_Relational_Operators {
    public static void main(String[] args) {
        // < Less than
        // > greater than
        // <= less than or equal to
        // >= greater than or equal to
        // == Equal to (checking)
        // != Not equal to
        // All of them will result boolean output - true or false only

        int a = 10;
        int b = 30;
        boolean c = a>b;
        System.out.println(c);

        int age_Vini = 30;
        int age_Karthi = 30;
        boolean result = age_Vini >= age_Karthi;
        System.out.println(result);

    }

}
