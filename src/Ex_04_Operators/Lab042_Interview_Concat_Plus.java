package Ex_04_Operators;

public class Lab042_Interview_Concat_Plus {
    public static void main(String[] args) {
        // concatination - add the value
        System.out.println("Vinitha" + "Shanugavelayutham");
        //Int ex
        int a = 10;
        int b = 20;
        System.out.println(a+b);
        //String ex
        String name1 = "Vini";
        String name2 = "Shanmugavelayutham";
        System.out.println(name1+name2);
        // + -> behave differently with data type, for integer it adds the value, for String it concat  the character and display it
        // + -> operator overloading
    }
}
