package ex_10_For_Loop;

public class Lab109_ForLoop {
    public static void main(String[] args) {
        for (int i = 0; i < 10; i++) // 0 to 9
        {
            System.out.println(i); // 10 times
        }
        for (int j = 1 ; j <= 10; j++) // 1 to 10
        {
            System.out.println(j); // // 10 times
        }
    }
}
