package ex_10_For_Loop;

public class Lab098_For_Loop {
    public static void main(String[] args) {
        // For loop
        // Help you to repeat a block of code
        // Initialization -> Condition -> Updation (Incre/Decre) ICU
        // ICU - order will be always same, can't chnage first CIU like this

        for (int i=0; i<10; i++)
        { // 0,1,2...9 , 10 times executed
            // incremen/dec operator in case of for loop does not make any difference
            System.out.println(i);
        }
    }
}
