package ex_10_For_Loop;

public class Lab103_For_IF_Loop {
    public static void main(String[] args) {
        for (int vini = 0; vini < 18; vini++) // 0 to 17, 18 times
        {
            if (vini > 15){
                System.out.println("Gift from Appa, IPhone");
            }
            else {
                System.out.println("No Gift");
            }
        }
    }
}
